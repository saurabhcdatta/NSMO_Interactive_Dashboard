nsmo_format_coding <- list(
  # 1=Very/Satisfied/Likely/Open/Helpful, 2=Somewhat, 3=Not at all
  PVERYFM   = list(pos = c(1, 2), neg = c(3)),
  # 1=A Lot, 2=A Little, 3=Not At All
  PLOTFM    = list(pos = c(1, 2), neg = c(3)),
  # 1=Yes, 2=No  (most binary survey items)
  PYNFM     = list(pos = c(1),    neg = c(2)),
  # 1=Important, 2=Not important
  PIMPFM    = list(pos = c(1),    neg = c(2)),
  # 1=Agree, 2=Disagree
  PAGREEFM  = list(pos = c(1),    neg = c(2)),
  # 1=Used, 2=Not Used
  PUSEDFM   = list(pos = c(1),    neg = c(2)),
  # 1=Before Offer, 2=After Offer, 3=Did not do → did it (before or after) = positive
  PTIMEFM   = list(pos = c(1, 2), neg = c(3)),
  # 1=Sig Increase, 2=Little/No Change, 3=Sig Decrease → stable/up = positive
  PCHANGEFM = list(pos = c(1, 2), neg = c(3)),
  # x51: 1=Higher (bad), 2=Same, 3=Lower → same or lower cost = positive
  PSAMEFM   = list(pos = c(2, 3), neg = c(1))
)

preprocess_data <- function(df) {
  # Detect survey response variables: x-prefix, numeric, ≤10 distinct
  # valid values (1-10). Negative codes like -2, -3 are N/A markers and
  # are intentionally excluded from the uniqueness/range check.
  response_vars <- names(df)[grepl("^x[0-9]", names(df)) &
    sapply(df, function(x) {
      vals <- na.omit(x)
      pos_vals <- vals[vals > 0]   # ignore -2, -3 not-applicable codes
      is.numeric(x) &&
        length(pos_vals) > 0 &&
        length(unique(pos_vals)) <= 10 &&
        all(pos_vals %in% 1:10)
    })]

  # Apply format-aware binary coding to each response variable
  df_processed <- df
  for (v in response_vars) {
    fmt    <- nsmo_var_format[v]
    coding <- if (!is.na(fmt) && !is.null(nsmo_format_coding[[fmt]])) {
      nsmo_format_coding[[fmt]]
    } else {
      list(pos = c(1, 2), neg = c(3))   # safe default: Very/Somewhat = positive
    }
    raw <- df_processed[[v]]
    df_processed[[paste0(v, "_binary")]] <- case_when(
      raw %in% coding$pos ~ "Positive (Very/Somewhat)",
      raw %in% coding$neg ~ "Negative (Not at all)",
      TRUE                ~ NA_character_
    )
  }

  df_processed <- df_processed %>%
    mutate(
      Age           = coalesce(age_o1, age_o2),
      # Age_Category: prefer survey self-report (x74r); fall back to numeric Age from Experian
      Age_num       = coalesce(age_o1, age_o2),
      Age_Category  = case_when(
        x74r == 1 ~ "18-24", x74r == 2 ~ "25-34", x74r == 3 ~ "35-44",
        x74r == 4 ~ "45-54", x74r == 5 ~ "55-64", x74r == 6 ~ "65-74",
        x74r == 7 ~ "75+",
        !is.na(Age_num) & Age_num >= 18 & Age_num < 25 ~ "18-24",
        !is.na(Age_num) & Age_num >= 25 & Age_num < 35 ~ "25-34",
        !is.na(Age_num) & Age_num >= 35 & Age_num < 45 ~ "35-44",
        !is.na(Age_num) & Age_num >= 45 & Age_num < 55 ~ "45-54",
        !is.na(Age_num) & Age_num >= 55 & Age_num < 65 ~ "55-64",
        !is.na(Age_num) & Age_num >= 65 & Age_num < 75 ~ "65-74",
        !is.na(Age_num) & Age_num >= 75                ~ "75+",
        TRUE ~ "Unknown"),
      Sex       = coalesce(sex_o1, sex_o2),
      Sex_Label = case_when(
        Sex == 1 | x75r == 1 ~ "Male",
        Sex == 2 | x75r == 2 ~ "Female",
        TRUE ~ "Unknown"),
      Education_Max   = pmax(x76r, x76s, na.rm = TRUE),
      Education_Label = case_when(
        Education_Max == 1 ~ "Some High School",
        Education_Max == 2 ~ "High School Graduate",
        Education_Max == 3 ~ "Tech School",
        Education_Max == 4 ~ "Some College",
        Education_Max == 5 ~ "College Graduate",
        Education_Max == 6 ~ "Post-Graduate",
        TRUE ~ "Unknown"),

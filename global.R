v2_plotly_layout <- function(p, title = NULL, xlab = NULL, ylab = NULL,
                              legend_bottom = TRUE, margin = list(t = 50, b = 60, l = 60, r = 30)) {
  p <- p %>% layout(
    font        = list(family = "Inter, Segoe UI, sans-serif", color = v2_colors$text_primary),
    paper_bgcolor = "rgba(0,0,0,0)",
    plot_bgcolor  = "rgba(0,0,0,0)",
    margin        = margin,
    xaxis = list(showgrid = FALSE, zeroline = FALSE, title = xlab,
                 tickfont = list(size = 11)),
    yaxis = list(showgrid = TRUE, gridcolor = "#E2E8F0", zeroline = FALSE, title = ylab,
                 tickfont = list(size = 11))
  )
  if (!is.null(title)) {
    p <- p %>% layout(title = list(text = title, font = list(size = 14, color = v2_colors$text_primary)))
  }
  if (legend_bottom) {
    p <- p %>% layout(legend = list(orientation = "h", x = 0.3, y = -0.25,
                                    font = list(size = 11)))
  }
  p
}

